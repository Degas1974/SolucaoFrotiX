                        vm.MotoristaId ,
                        vm.Nome ,
                        vm.Ponto ,
                        vm.CNH ,
                        vm.Celular01 ,
                        vm.CategoriaCNH ,
                        Sigla = vm.Sigla != null ? vm.Sigla : "" ,
