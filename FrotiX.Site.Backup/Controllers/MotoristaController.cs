        {
            try
            {
                // [VALIDACAO] Confirma ID vÃ¡lido.
                if (Id != Guid.Empty)
                {
                    // [DADOS] Carrega motorista.
                    var objFromDb = _unitOfWork.Motorista.GetFirstOrDefault(u => u.MotoristaId == Id);
