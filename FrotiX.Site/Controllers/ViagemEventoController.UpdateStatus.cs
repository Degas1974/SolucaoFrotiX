        /****************************************************************************************
         * ‚ö° FUN√á√ÉO: UpdateStatusEvento
         * --------------------------------------------------------------------------------------
         * üéØ OBJETIVO     : Alternar status do evento entre Ativo ("1") e Inativo ("0")
